BEGIN
DECLARE s VARCHAR(1000);
DECLARE w VARCHAR(1000);
SET s="select DISTINCT hphm,(select nodename from node where num=ddbh_1) as ddbh_1,gcsj_1,(select nodename from node where num=ddbh_2) as ddbh_2,gcsj_2,distance,time,speed from suspect_record";
SET w=" where 1=1";

IF city is not null and city <> "" then 
  IF district is not null and district <> "" then 
  SET s=CONCAT(s,",(select num from node where city='",city,"' and district='",district,"') nums ");
  SET w=CONCAT(w," and (ddbh_1=nums.num or ddbh_2=nums.num)");
  END IF;
END IF;

IF (BeginTime is not null and BeginTime <> "") or (EndTime is not null and EndTime <> "") then 
  SET w=CONCAT(w," and gcsj_1 like'",BeginTime,"%' and gcsj_2 like'",EndTime,"%'");
END IF;

IF speed is not null and speed <> "" then
  SET w=CONCAT(w," and speed>",speed);
END IF;
SET @sw=CONCAT(s,w);
PREPARE stmt FROM @sw;
EXECUTE stmt;
END
